package com.dam.eva.listviewrec;

public class Bloc {

    String dataa;
    String tempe;
    String fredCalor;

    public Bloc(String dataa, String tempe, String fredCalor) {
        this.dataa = dataa;
        this.tempe = tempe;
        this.fredCalor = fredCalor;
    }

    public String getDataa() {
        return dataa;
    }

    public void setDataa(String dataa) {
        this.dataa = dataa;
    }

    public String getTempe() {
        return tempe;
    }

    public void setTempe(String tempe) {
        this.tempe = tempe;
    }

    public String getFredCalor() {
        return fredCalor;
    }

    public void setFredCalor(String fredCalor) {
        this.fredCalor = fredCalor;
    }
}
